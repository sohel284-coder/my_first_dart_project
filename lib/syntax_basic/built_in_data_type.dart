void dartDataType() {

  // numbers
  int age = 25; // integer
  double salary = 45.00;

  // String
    String name = "Alice";

  // Booleans
    bool isStudent = false;

  // lists
  List<String> fruits = ["Apple", "Banana", "Orange"];
  List<int> nums = [3, 4];

  Map<String, int> scores = {"Alice": 90, "Bob": 85};

  print(age); // 25
  print(salary); // 19.99
  print(name); // Alice
  print(isStudent); // true
  print(nums);
  print(fruits); // [Apple, Banana, Orange]
  print(scores); // {Alice: 90, Bob: 85}
  
}
